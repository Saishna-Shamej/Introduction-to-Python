#no duplicates allowed
thisset = {"apple", "banana", "apple", "cherry"}
print(thisset)
print()

#True and 1 is considered as the same
thisset = {"apple", "banana", "cherry", True, 1, 2}
print(thisset)
print()

#length of a set
thisset = {"apple", "banana", "cherry"}
print(len(thisset))
print()

#set can be of any type, type of set
myset = {"apple", "banana", "cherry"}
print(type(myset))
print()

#set constructer
thisset = set(("apple", "banana", "cherry"))
print(thisset)
print()
